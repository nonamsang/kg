package com.moodshop.kokone.vo;

public class ProductOptionVO {
	private String option_id;
	private String product_id;
	private String option_color;
	private String option_size;
	private int option_stock;
	private String option_picture;

	public String getOption_id() {
		return option_id;
	}

	public void setOption_id(String option_id) {
		this.option_id = option_id;
	}

	public String getProduct_id() {
		return product_id;
	}

	public void setProduct_id(String product_id) {
		this.product_id = product_id;
	}

	public String getOption_color() {
		return option_color;
	}

	public void setOption_color(String option_color) {
		this.option_color = option_color;
	}

	public String getOption_size() {
		return option_size;
	}

	public void setOption_size(String option_size) {
		this.option_size = option_size;
	}

	public int getOption_stock() {
		return option_stock;
	}

	public void setOption_stock(int option_stock) {
		this.option_stock = option_stock;
	}

	public String getOption_picture() {
		return option_picture;
	}

	public void setOption_picture(String option_picture) {
		this.option_picture = option_picture;
	}


}
